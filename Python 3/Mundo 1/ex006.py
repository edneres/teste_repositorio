# Crie um algoritmo que leia um número          #
# e mostre o seu dobro, triplo e raiz quadrada. # 
# # # # # # # # # # # # # # # # # # # # # # # # #

num = float(input("Digite um número: "))

dobro = num * 2
triplo = num * 3
raiz = num ** (1/2)

print("Dobro:           ", dobro)
print("Triplo:          ", triplo)
print("Raiz quadrada:   ", raiz)