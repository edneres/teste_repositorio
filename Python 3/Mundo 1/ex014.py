# Escreva um programa que converta uma temperatura digitada em  #
# graus Celsius e converta para graus Fahrenheit.               #
# # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # #

Celcius = float(input("Digite uma temperatura em graus Celcius: "))
Fahrenheit = (Celcius * 1.8) + 32

print("A temperatura em Fahrenheit é {:.2f} graus".format(Fahrenheit))